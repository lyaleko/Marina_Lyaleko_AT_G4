package tasks.homework.cycletask;

// используя while вывести в консоль все числа от 0 до 20 в 1 строку через пробел
public class RangeNumbersWhile {

    public void methodRangeNumbersWhile() {
        int i = 0;
        while (i < 21) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println();
    }

}